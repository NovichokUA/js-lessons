// Події клавіатури
// Існує дві основні події клавіатури:

// keydown — подія, що відбувається при натисканні клавіші
// keyup — подія, що відбувається, коли клавішу відпустили

// На відміну від інших подій, події клавіатури обробляються на документі, а не
// на конкретному елементі.Об'єкти подій клавіатури походять від базового класу
// KeyboardEvent.

